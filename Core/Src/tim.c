void HAL_TIM_IC_CaptureCallback(TIM_HandleTypeDef *htim)
{
  uint8_t hwSignalIdx;
  
  for (hwSignalIdx = (uint8_t)0U; hwSignalIdx < IOMANAGER_SIGNAL_PIN_NUM; hwSignalIdx++)
  {
    if ((htim->Instance == (TIMx_hwSignalCfg[hwSignalIdx].hwTimerIns)->Instance) && \
        (htim->Channel  ==  TIMx_hwSignalCfg[hwSignalIdx].hwTimerActICChn))
    {
      IoManager_ExtIrptCallback(IOMANAGER_SIGNAL_IC_THREAD_FLAG | hwSignalIdx);
      break;
    }
  }
}



